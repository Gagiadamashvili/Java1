let month = "";

switch (month) {
    case "March":
    case "April":
    case "May":
        console.log("Spring")
        break;
    case "June":
    case "July":
    case "August":
        console.log("Summer")
        break;
    case "September":
    case "October":
    case "November":
        console.log("Autumn")
        break;
    case "December":
    case "January":
    case "February":
        console.log("Winter")
    default:
        console.log("This's not month")
}